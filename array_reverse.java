public class array_reverse {
    public static void main(String[] args) {
        System.out.println("to print an array in the reverse order:");

        int[] arr2 = {10, 20, 30, 40,50};
        for(int i = arr2.length - 1; i>=0; i--){
            System.out.println(arr2[i]);
        }
        
    }
}
